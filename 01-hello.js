console.log(5 + 3) // 8 (加法)
console.log(5 - 3) // 2 (減法)
console.log(5 * 3) // 15 (乘法)
console.log(5 / 2) // 2.5 (除法)
console.log(5 % 3) // 2 (取餘數)
console.log(2 ** 3) // 8 (次方)
console.log(10 / 0) // Infinity
console.log(-10 / 0) // -Infinity
console.log(0 / 0) // NaN
// 相等比較
console.log(1 + 2 == 3) // true
console.log(123 % 10 == 3) // true

// 範圍比較
console.log(5 > 3) // true
console.log(5 >= 5) // true
console.log(3 < 5) // true
console.log(3 <= 3) // true

// 嚴格相等比較
console.log(5 === 5) // true (值和型別都相同)
console.log(5 === '5') // false (值相同，但型別不同)






